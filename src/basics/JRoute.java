private void OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKActionPerformed
// TODO add your handling code here:
    String citystart = CitystartText.getText();
    String cityend = CityendText.getText();
    
    String d = (String)Depdatemera.getSelectedItem();
    int day = new Integer(d).intValue();
    
    String m = (String)Depdatemin.getSelectedItem();
    int month = new Integer(m).intValue();
    
    String y = (String)Depdatexronia.getSelectedItem();
    int year = new Integer(y).intValue();
    
    Date depdate = new Date(day,month,year);
    
    String eh = (String)Earlydepora.getSelectedItem();
    int ehour = new Integer(eh).intValue();
    
    String el = (String)Earlydeplepta.getSelectedItem();
    int emin = new Integer(el).intValue();
    
    Time earlydep = new Time(ehour,emin,0);
    
    String lh = (String)Latedepora.getSelectedItem();
    int lhour = new Integer(lh).intValue();
    
    String ll = (String)Latedeplepta.getSelectedItem();
    int lmin = new Integer(ll).intValue();
    
    Time latedep = new Time(lhour,lmin,0);
    
    String dmidstops = MidstopsText.getText();
    String[] tokenm = dmidstops.split(",");
    String[] midstops = new String[5];
    System.arraycopy(tokenm, 0, midstops, 0, tokenm.length);
    
    
    Demo3.r = new Route(citystart, cityend, depdate, earlydep, latedep, midstops);
    
    
    JCar j = new JCar();
    j.setVisible(true);
    
    this.dispose();
    
}//GEN-LAST:event_OKActionPerformed

